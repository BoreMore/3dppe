                                        <h3>About Us &nbsp </h3>
                                        <p>
                                            3DPPE was started by a group of students in Wheeler High School with 3D printers and a desire to help medical professionals in the front lines of the coronavirus pandemic. 
                                            It started as a group of only a few people, but after seeing the dire need for these masks, it quickly grew to be 30+ people.
                                            <br><br>
                                            We started the group on March 25th, but after realizing the dire need medical professionals have for the masks, we began contacting people to get the word out.
                                            <br><br>
                                            Northview High School students Richard Kim and Michelle Heo and former Northview High School students Lucas Moyer and Calvin Booth greatly expanded 3DPPE's reach by reaching out to nonprofit organizations such as Masks for Docs and ANLF (Asian New Life Foundation) in order to deliver masks outside of the state of Georgia. They've coordinated deliveries to Texas, Puerto Rico, Mexico, New York, and Florida. 
                                        </p>
